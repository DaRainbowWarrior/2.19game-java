package twonineteengame.model;

/**
 * Stores the types of pieces possible on the board.
 */
public enum Value {
    /**
     * Represents a red piece / the red player.
     */
    RED,

    /**
     * Represents a red piece / the blue player.
     */
    BLUE,

    /**
     * Represents an empty spot on the board.
     */
    EMPTY
}